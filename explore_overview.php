                    unique in a resort town  --- a real neighborhood
                    with real full-time  residents and real sense
                  of community and place.</p>
                  <p style="text-align:justify;">What  makes the Wellington Neighborhood							&quot;real&quot;  and creates a real &quot;sense of community and  place?&quot;</p>
                  <p style="text-align:justify;">Perhaps it is the award  winning design --- architectural
							patterns drawn from  early
							Breckenridge, narrow  streets, alleys, greens, and
							plentiful connections.</p>
							    <p>Perhaps it's the  neighborhood's location in historic
							French Gulch. Around  1860, miners came
							and proceeded to settle  French Gulch. Over the
							next century French  Pete, Ben Stanley Revett,	
							Harry Farncomb, Barney  Ford and even the
							Phipps family of Denver  Bronco fame, sought
							riches in French Gulch.  The largest nugget ever
							found in Colorado --- Tom's Baby --- came out
							of French Gulch and now  is on display in the
							Denver Museum of Nature and Science.</p>
							<p>Perhaps it's the  neighborhood's location in
							Breckenridge --- an  unpretentious historic town
							with diverse community  facilities including the
							The Riverwalk Center (home to the
							Breckenridge Music  Festival), the Stephen C.
							West Ice Arena (host to  a NHL pre-season training
							camp), the Breckenridge Recreation Center 
							(with a 25 yard indoor  pool, water slide, climbing
							wall, tennis courts and  sports rehabilitation
							center), a Jack  Nicklaus designed, 27-hole golf
							course, and skiing that  has consistently ranked
							the mountain as one of  the top two most popular
							resorts in North America.</p>
							<p><span class="floatRight"><img src="images/footer_neighborhood2.jpg" alt="neighbor" width="200" height="125"></span>Perhaps it's the  neighborhood's location within
							the town limits yet  immediately adjoining the
							largest open  space acquisition in the
							history of Summit County --- 1,840 acres of land
							stretching from the  eastern boundary of the
							neighborhood and  wrapping around through the
							Golden Horseshoe - an  area rich in history, habitat
							and recreational  opportunities. Literally out
							your back door are some  of the best mountain
							bike trails in Colorado. And just up the valley,
							genetically pure  cutthroat trout.</p>
							<p>Perhaps it is the  quality of full time residents
							who make the Wellington  Neighborhood their
							home. The neighborhood  has a diverse group of
							permanent residents  including a town manager,
							district attorney,  small business owners,
							lawyers, nurses, police  officers and teachers.
							Although diverse, they  all share a common
							interest in small town  quality of life and the
							seemingly unlimited  array of mountain sports
							and activities.</p>
							<p>Or, maybe it's just the  unobstructed views of the
							Breckenridge ski area  and the Ten Mile Range&hellip;</p>
					 		<p>In any event, at a time  when new mountain
							development typically  consists of contrived
							Bavarian villages ---  the Wellington 
							Neighborhood is  different. It is a real neighborhood
							with a real sense of  community and place.</p>
