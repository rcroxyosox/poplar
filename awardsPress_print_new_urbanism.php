		   <h2>Wellington Neighborhood to make move toward textbook 'new urbanism'</h2>
	    </center>
		 <p><b>By M. John Fayhee</b></p>
		 <p><b>Live/work segment of project moving forward</b></p>
		 <p>David O'Neil, developer of Breckenridge's Wellington Neighborhood, understands full well that, ever since the award-winning project was first presented to the town in 1997, he has had to carry a full pocket of asterisks every time he refers to it as "new urban" in nature and structure. Sure, Wellington has, from the get-go, boasted many of the concepts put forth by the Congress for New Urbanism. There are common areas and front yards and porches and picket fences and all the garages are right where all tasteful vehicle enclosures ought to be: out back, near the alley, where they can't be seen as easily.</p>
		 <center>
		   <p><img src="images/photo_textbook.jpg" width="300" height="190" alt="" border="1">
  <center>Wellington Neighborhood</center></p>
     </center>
		 <p>So far, so good. But Wellington has been missing one of the basic components of the New Urbanism liturgy: Commercial space integrally mixed right in with the rest of the development.</p>
		 <p>It's not that O'Neil has been playing some sort of conceptual/marketing shell game. On-site commercial space has been part of the long-range plan since Wellington was first conceived. It's just been a case of making certain the cart does not inch its way up ahead of the horse.</p>
		 <p>Now, with 60 occupied homes to his credit and 150 or so people already calling Wellington home, O'Neil believes it's time to start seriously planning the commercial phase of the project. And, like the residential component, the commercial part of Wellington will set conception standards aplenty.</p>
		 <p>What O'Neil envisions is something called "Live/work." To give you some idea of how important he considers the synthesis of residential and commercial space, his fact sheet does not even divide the two words. He spells it "Livework" - a situation where at least some of Wellington's residents can literally stroll on over to the commercial component of the development and punch in at the ol' timeclock.</p>
		 <p>The idea is as old as the concept of New Urbanism itself. Historically, American neighborhoods contained just about everything residents could need: grocery stores, bakeries, coffee shops, bars etc.--all within a 10-minute walk from home. Many people lived above, or at least within walking distance, of their place of employ. And, even those people who commuted to work did not need to commute to buy the basics of life. It has only been in the past half-century that our living structure has devolved to the point that people have to drive 25 miles to get a beer or a bag of taters.</p>
		 <center>
		   <table width="300" cellspacing="0" cellpadding="0" border="0">
		     <tr>
		       <td width="300"><font size="-1"><p><img src="images/photo_textbook1.jpg" width="300" height="202" alt="" border="1">
		         <br>As it stands, the 150 or so residents of the Wellington Neighborhood still have to drive a couple miles to belt a latté or have a cup of java. When the development's commercial sector is developed, they will be able to quaff their thirst right in their own backyard.</p>
			  <p>Photo By Mark Fox</p></td>
		  </tr>
	      </table>
	    </center>
		 <p>"The idea of having a commercial aspect to Wellington has been there the whole time," O'Neil says. "The Breckenridge Planning Department knows that we intend to bring that part of the project before them."</p>
		 <p>Wellington Neighborhood, located about three miles up Wellington Road (turn at the 7-Eleven just north of Breckenridge and keep driving until you see a whole slew of faux Victorian houses on your right), consists of 85 acres. The project is already approved for another 60 housing units. There are three lots specifically dedicated to O'Neil's Livework "experiment" (his word).</p>
		 <p>"The first building we have designed will be 2,400 square feet, which is not large," O'Neil says. "The main floor will have 1,400 square feet, which will be used for a bakery and coffee house, post office boxes, ski storage and bus stop. The second floor will be used for office space. I'm already talking to a long-time local about running the bakery and coffee house, and several residents of Wellington who work at home have expressed interest in the second-floor offices. I think there will be 8 125-square-foot offices.</p>
		 <p>"There will be two other buildings, one the same size as the first, and the last one will probably be about a third the size," O'Neil continues. "These buildings will not jut be for residents of Wellington, though I expect many will be interested in this aspect of the project. The goal is to enhance the traditional neighborhood character of the Wellington Neighborhood. The architecture will fit with the rest of the neighborhood, and the business mix will give people the opportunity to make connections with their neighbors."</p>
		 <center>
		   <table width="430" cellspacing="0" cellpadding="5" border="0">
