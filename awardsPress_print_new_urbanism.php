	    </center>
		 <p>O'Neil says his fantasy is to have people returning to Wellington via the Summit Stage, stopping to pick up their mail, then going into the java hut for an espresso and to mingle with neighbors, before walking home.</p>
		 <p>"We're definitely looking to round out the neighborhood experience," he says.</p>
		 <p>The fiscal structure of the commercial aspect of Wellington is, well, not traditional. Matter of fact, it's not even fully sussed out yet.</p>
		 <p>"We are hoping that people interested in the commercial space might have an ownership interest in the building," O'Neil says. "Our lease rates will be influenced by what users bring to the neighborhood. This is still a work-in-progress. We'll see what kind of interest there is in the project."</p>
		 <p>No matter how the fiscal component of the project eventually pans out, one thing is clear: O'Neil has a palpable mix vision of Wellington's commercial space.</p>
		 <p>"I'd love to see things like an antique store, a small wine store, a gardening store, offices for architects, dentists, healing arts, an art gallery, some cottage industry, like a snowshoe or kite manufacturer, or maybe even a newspaper office," he says. "Whatever goes in there, we want it to enhance the experience of living here."</p>
		 <p>Once O'Neil gets a better feel for what kinds of businesses might actually occupy his commercial space, he will go before the Breckenridge Planning Department.</p>
		 <p>"They know we're coming," he says. "I don't foresee any problems. I'd really like to break ground by the end of the summer, with first occupancy taking place as early as late fall."</p>
		 <p>The commercial space is not the only new concept being integrated into Wellington's plans. According to O'Neil, there will also be some sort of community building.</p>
		 <p>"It could end up being a fire station or a church," he says.</p>
		 <p>For more information on Wellington Neighborhood, which is featured in the current issue of Metropolitan Home Magazine, go to poplarhouse.com.</p> 
