		  <p><img src="images/singlefamily.jpg" alt="singleFamily" width="205" height="154">Resort communities, such as Breckenridge, face a serious challenge in creating affordable neighborhoods that serve the needs of employees, particularly the needs of first time home buyers and young families. Certainly rapidly appreciating real property values are a major problem; however, &quot;neighborhoods&quot; dominated by rentals and second home owners also pose a challenge.</p>
		  <p>To address these concerns, we have worked with staff and the Breckenridge Town Council to create a program which balances the interests of the Town, the homeowners (current and future), the founders, and mortgage lenders and is simple enough to be easily administered.</p>
		  <p>We have created a program which provides that where the &quot;local price&quot; is paid:</p>
		  <p>a) House owners must be individuals, work 30 hours per week in Summit County, and reside in the house (if a unit is not owner occupied for a period greater than twelve months, the owner would be obligated to offer the unit for sale at the highest allowed sales price); and </p>
		  <p>b) Appreciation will be limited to 3% per annum or the percentage increase in AMI (Area Median Income), whichever is greater.</p>
		  <p>80% of the units will have the deed restriction. 20% of the units will be unrestricted &quot;market&quot; units.</p>
