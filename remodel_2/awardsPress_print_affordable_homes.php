		    <h2>Breckenridge project offers affordable homes</h2>
  
		<p><b>Victorian-style village aimed at local workers</b></p>
		  </center>
		<p>By Jason Blevins 
        <br>Denver Post Business Writer</p>
		<p><b>Tuesday, August 14, 2001 - BRECKENRIDGE</b> - David O'Neil is not a chef, but he thinks he's got a pretty good recipe.</p>
		<p><img src="../images/bizwell0814.jpg" width="242" height="261" alt="" border="0" align="left">In the mix: plenty of affordable housing with a few market-priced homes, local employees and second homeowners, Victorian architecture with porches overlooking shared front yards, and a network of trails connecting to acres of Forest Service land.</p>
		<p>O'Neil is creating a new kind of community along the banks of French Creek a mile east of downtown Breckenridge. </p>
		<p>O'Neil and partners Tom Lyon and John Wolff are building a Victorian-style village of 122 homes - 98 of which are being offered to local workers at deed-restricted prices. The new neighborhood is called Wellington. </p>
		<p>"This is the only way we could afford to live here," said George Hughes, a sergeant with the Breckenridge Police Department, who was framing his new garage behind the home he shares with his wife, a florist. </p>
		<p>Hughes estimates that 60 percent of his co-workers in the department travel to work daily from homes over Hoosier Pass in Park County. </p>
		<p>Wellington is more than just affordable housing, said Breckenridge Mayor Sam Mamula. Affordable housing often is simply a place to live - a spot far from town where working folk congregate, he said. </p>
		<p>"Wellington is just the opposite," Mamula said.  "Wellington has proven that you can have affordable within a neighborhood ... where people have a stake in the community." </p>
		<p>That's the whole idea, said O'Neil, who, also with Lyon and Wolff, built Boulder's award-winning Poplar Gardens - a 14-unit low-income housing project on less than two acres. Wolff and Lyon's projects also include the neighborhood at the old Elitch Gardens site in Denver and the first phase of development at the Stapleton Airport site. </p>
		<p>"Don't call us developers," O'Neil said. "We call ourselves town founders."</p>
		<p>Wellington, which will eventually host multi-unit homes and a few shops on 23 acres, is an example of the popular New Urbanism movement. </p>
		<p>Narrow streets with even narrower alleys separate clusters of 1,800-square-foot homes. Oblong lawns abut white picket fences surrounding homes with latticed porches. The houses, because of offset foundations and a variety of designs, do not line up in rows. The open areas connect to trails that meander through federal lands to old mining camps and ghost towns, all at nearly 10,000 feet. </p>
		<p>Proud of his work, O'Neil points to each of the 36 homes that have been finished, naming the owners and their jobs. There are nurses, a county attorney, teachers, a town administrator, shop owners, waiters, ski coaches and the greenskeeper at the local golf course. </p>
		<p>Each of the 32 homeowners in Wellington passed this simple test to get the homes: They work at least 30 hours a week in Summit County and agree that the value of their homes will be limited to the greater of either 3 percent a year or the percentage increase in the area's median income. </p>
		<p>For that, homeowners pay about $110 per square foot, compared with $400-$500 a square foot for homes less than a mile away. </p>
		<p>Twenty-four of the planned 122 homes will be sold at market value - close to $375,000 each. </p>
		<p>"It's perfect for the second homeowner who gets to come up to a full community," O'Neil said. "If they go to a regular ski-in-ski-out place, they never know who's next door." </p>
		<p>O'Neil and his partners have reserved 20 acres as open space, including a 5-acre parcel filled with 100-year-old blue spruce and a wide swath along French Creek. </p>
		<p>The trio owns another 40-plus acres across the creek, where O'Neil said a mirror of Wellington could emerge in the next six years. </p>
		<p>It wasn't an easy process to gain approval from the local planning staff, which was critical of the narrow streets and alleys. O'Neil spent more than a year haggling over the alleys, which are unheard of in high elevations because of the inability to accommodate snowplows. </p>
		<p>Planners wanted alleys to be 70 feet wide. O'Neil wanted something closer to 20 feet. They settled on 36-foot-wide alleys. The homeowners take care of snow removal. </p>
		<p>The New Urbanism principles are unfriendly to cars, with its narrow streets and off-street parking more pedestrian-oriented, said O'Neil, who winces when he talks about his fight to keep the streets and alleys as narrow as possible. </p>
		<p>"I think the neighborhood has turned out to be a great example of the public and private sectors working together," said Mamula. </p>
		<p>"David certainly had the vision, and he's carried through on it."</p>
		<p><a href="http://www.denverpost.com" onClick="openwin()" target="x">Denver Post</a> - August 14, 2001</p>
		 
