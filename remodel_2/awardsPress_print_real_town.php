		    <h2>Real town for real people</h2>
	    </center>
		  <p><b>Thursday, August 16, 2001</b> - Usually when we hear about new affordable housing units, we hear about a handful of less-expensive homes mixed in with many more being sold at market prices. In contrast, the new Wellington neighborhood just outside Breckenridge mixes 98 units priced at an attainable $150-$180 per square foot with 24 homes to be sold at market rates. In town, the same units would probably be priced at $300-$400 per square foot. </p>
		  <p>But the housing ratio isn't all that's exciting about Wellington, which also offers innovative architecture and a diverse accumulation of residents. </p>
		  <p>As writer Jason Blevins detailed on Tuesday's Business pages, Wellington has the flavor and charm of a Victorian village, with porches, gables and fretwork, picket fences, narrow streets and alleys and connections to the surrounding forests. </p>
		  <p>To accomplish that, Breckenridge city leaders had to take a significant leap of faith, abandoning standard land-use requirements to permit New Urbanist design solutions that allow more density and, therefore, more affordability. The result is a new community with the rare intimacy of traditional neighborhoods.</p>
		  <p>In addition to amending land-use codes, Breckenridge also offered a powerful incentive to developers David O'Neil, Tom Lyon and John Wolff. Under usual county zoning, Wellington's 85 acres would have entitled the builders to a mere four units. In addition to annexing the land to the city, the Breckenridge council upped the ante to permit 122 units - offering a much greater profit potential to the developers. </p>
		  <p>To qualify for one of the less-expensive units, buyers must work at least 30 hours a week in Summit County and agree to deed restrictions that limit increases in the resale value of their homes to the greater of 3 percent a year or the percentage increase in the area's median income. </p>
		  <p>The result, says O'Neil, is that "real people live here." Early buyers include Breckenridge's town manager, a county attorney, police officers, nurses, teachers, shop owners, waiters, ski coaches and the golf-course greenskeeper. That mix, O'Neil points out, is made possible only by the principle of affordability. But it pays off by giving Wellington much more vibrancy and stability than other, perhaps posher, mountain enclaves, where owners drop in to their second-home mini-mansions only on weekends or holidays. </p>
		  <p>A major challenge facing Colorado is finding ways to house the folks who keep our towns and cities humming. In Breckenridge, enlightened public-sector incentives and gutsy entrepreneurship have combined to provide a model for the entire state.</p>
		  <p><a href="http://www.denverpost.com" onClick="openwin()" target="x">Denver Post</a> - August 16, 2001</p>
