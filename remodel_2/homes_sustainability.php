		  <p><img src="images/sg_awards_logo.jpg" alt="Sustainability Award"></p>
          <p>Sustainable design and development is integrated into the core
thinking and methodology of the Wellington Neighborhood project team. In addition
to Wolff Lyon Architects’ extensive expertise, the
Wellington Neighborhood design / construction team 
worked with the Environmental Protection Agency and the
National Renewable Energy Laboratory to have the Wellington
affordable housing plans reviewed and critiqued to maximize
energy efficiency and sustainability.</p>
<p>An understanding of the benefits of sustainability has been part
of the Wolff Lyon tradition since their beginnings in the mid-70’s
when solar energy applications first captured the imagination
of designers and builders. Perhaps due to the firm’s location in
Boulder, which has taken the issue of sustainability seriously
for many years, energy efficiency, water conservation, indoor
air quality, and recycling have been important components of
our design solutions for years. Many techniques that are now
part of “Green Points” programs in many cities and the LEED
check list (such as solar orientation, passive solar gain, compact
and efficient building envelopes, recycling through construction,
indoor air quality measures, increased insulation levels, etc.) have
been standard practice for Wolff-Lyon Architects for some time.
In the last ten years Wolff-Lyon attention to good sustainable design has
also been directed to the community level where they have been
strong advocates of an urbanism that promotes a compact, diverse,
walkable, affordable, mixed-use urban form. They have promoted
master plans and design standards that through mixed-use and
mixed-density create neighborhoods that are less dependent on
fossil fuels. And, they look for opportunities where an individual design
approach may have multiple benefits. For instance neighborhood
design solutions that incorporate narrower streets may help
reduce infrastructure costs, reduce car speeds increasing safety
and livability, and encourage residents to walk more while using
their cars less.</p> 

<p>At Wellington, we have taken an area that has been damaged by historic mining activity, reclaimed it and then created a neighborhood with a sense of community and place.   We have also explored innovative construction practices to minimize our environmental impact.  For instance, almost all road base and select back fill have been crushed and manufactured on site; thereby eliminating literally thousands of truck trips through the adjoining French Creek Village neighborhood.   Homes, streets and alleys have been oriented to provide for maximum solar benefit thereby providing warmth to homes and promoting snow melt on streets and alleys.   Particular attention has been provided to building envelopes as well as indoor appliances.</p>

<p>Finally, perhaps 80% of any neighborhood’s carbon footprint comes from the residents’ automobile commuting patterns.   By being located on public transportation within a couple of miles of downtown, City Market, the Recreation Center and the gondola, the Wellington Neighborhood provides the opportunity to minimize, if not all but eliminate, reliance on the automobile.  
</p>
