		<p><b>ROCKY MOUNTAIN VILLAGE</b></p>
		</center>
		
		<p><b>Challenge:</b> Persuade the town to adopt denser development on an 85-acre site originally zoned for four large residences and design a community that's child-friendly, walkable, affordable, and authentically tied to its region. The land consisted of dredge piles left over from mining operations.</p>

		<p><b>Solution:</b> Developer David O'Neil and architects Tom Lyon and John Wolff spent three years convincing authorities that the Wellington Neighborhood, a carefully designed scheme for 122 mostly single-family residences, would enhance the town and avoid sprawl. It feels like a Currier &amp; Ives village come to life. More than 70 freestanding two-story houses in six configurations - from two-bedroom duplexes of 1,000 square feet to four-bedroom dwellings of 1,800 square feet - have been built so far.</p>

		<p><b>MERIT AWARD</b> - Wolff Lyon Architects, Boulder, CO (303/447-2786)</p>
		
