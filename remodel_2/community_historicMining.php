		  <p><img src="images/dredgeboat.jpg" alt="dredgeboat" width="284" height="150">Nearby Wellington-Oro Mine And Environmental Impacts From Historic Mining Activities <br>
          <br>
		    While the materials on the property consist primarily of dredge piles containing native river rock, the historic Union Mill Site and Wellington-Oro Mine located upstream from the Wellington Neighborhood have garnered the attention of the United States Environmental Protection Agency. Between 1887 and 1972 the Wellington and Oro Mines (which were connected underground, and were worked together since 1903) provided millions of dollars worth of zinc, lead, silver, and gold, making the Wellington-Oro Mine the largest producing mine in the Breckenridge Mining District, and Summit County. These mining activities in combination with dredge boats looking for gold severely disturbed the landscape and habitat of French Creek.</p>
		  <p>Specifically, the EPA has been concerned with the existence of surface wastes which may pose a human health hazard in the immediate area of the Union Mill Site and Wellington-Oro Mine (away from the area proposed for development); and second, concentrations of heavy metals in ground water flowing from the Wellington-Oro underground mine workings which flow into French Creek and pose a threat to downstream trout habitat.</p>
		  <p>With respect to the Union Mill and Wellington-Oro Mine surface and water issues as may concern the Wellington Neighborhood, the neighborhood founders have completed a Prospective Purchaser Agreement with the EPA, the United States Department of Justice and the Colorado Department of Public Health and Environment benefiting the Wellington Neighborhood. The benefits of the Prospective Purchaser Agreement are twofold: a) through the PPA the federal and state governments provide a &quot;covenant not to sue&quot; thereby limiting environmental liability; and b) as part of the process to enter into the PPA, the EPA and CDPH&amp;E have undertaken various tests and reviewed data concerning the site, including a Phase I Environmental Site Assessment commissioned by the founders. Copies of the Phase I Environmental Site Assessment and the PPA are available upon request.</p>
		  <p>While environmental issues concerning the Wellington-Oro Mine have been raised, development of the Wellington Neighborhood site does offer some environmental benefits, including:</p>
		  <ul>
		    <li>The provision of land reserved or permanently dedicated for public use;</li>
        <li> Numerous trails and paths to provide access from the Wellington Neighborhood and adjoining neighborhoods to the back country;</li>
		    <li> Creation of a strong community edge between urban and rural uses;</li>
		    <li> Stopping a trend towards regional urban sprawl, which is recognized as a major factor in energy usage and wasting of land resources;</li>
		    <li> Reclamation of existing mine tailings (restoring damaged land for housing needs instead of destroying existing tree stands or meadows); and</li>
		    <li> Reducing Upper Blue Basin air pollution through reduced vehicle miles traveled, reduced use of gasoline products, reduced hydrocarbons and reduced dust. </li>
		  </ul>
