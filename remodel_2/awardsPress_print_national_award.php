		  <h2>Breck receives national award</h2>
		  </center>
		<p><b>November 19, 2002</b></p>
		<p><b>Jane Stebbins</b></p>
		<p>BRECKENRIDGE - Environmental Protection Agency (EPA) officials awarded the town of Breckenridge with a 2002 National Award for Smart Growth Achievement during a ceremony Monday in Washington, D.C.</p>
		<p>The town of was one of four honored for their innovative approaches to achieve smart growth. Each award incorporated principles of smart growth to create places that respect community culture and the environment, foster economic development and enhance quality of life.</p>
		<p>The EPA defines smart growth as growth that serves the economy, the community and the environment. Smart growth development practices support national environmental goals by preserving open space and park land and protecting critical habitat; improving transportation choices, including walking, bicycling, and public transit, which reduces emissions from automobiles; reducing polluted runoff; and promoting "Brownfield" redevelopment. Brownfields are described as lands with hazardous materials or other contaminants on them.</p>
		<p>The EPA recognizes projects in the categories of overall excellence in smart growth, built projects, policies and regulations and community outreach and education. Breckenridge Mayor Sam Mamula received the award on behalf of the town in the category of built projects.</p>
		<p>That category recognizes smart growth development already on the ground. Eligible projects include single or multiple buildings, single- or multi-family residential, retail and office; or mixed-use on greenfield, infill or transit-oriented sites.</p>
		<p>The EPA received 102 applications, making the selection difficult, said EPA Administrator Christine Todd Whitman. Criteria included how replicable the projects are, how effective they are in advancing smart growth and the level of citizen and stakeholder participation or partnerships.</p>
		<p>The Wellington Neighborhood provides affordable and market-rate housing on a site that two years ago was a pile of river rocks dredge boats left behind after mining for gold throughout the area. The project recycles land, creates housing for working families and helps the region avoid mountain sprawl, Whitman said.</p>
		<p>The neighborhood soon will be served by free transit to the nearby downtown via an agreement between county and town transportation departments.</p>
		<p>The development has reclaimed 22 acres of this so-called Brownfield site to construct the 122-home neighborhood, a compact community built in the style of traditional neighborhoods. Fifty-eight homes have been built to date. An additional 20 acres is preserved as open space or community parks.</p>
		<p>The Wellington Neighborhood was designed by Wolff-Lyon Architects of Boulder. Developer David O'Neil and Wolff-Lyon worked with federal, state and town officials and town residents for two years.</p>
		<p>"Ultimately, Breckenridge had the vision to encourage the project by allowing unprecedented flexibility with respect to zoning and road standards," O'Neil said. "The town also created indirect subsidies such as (waiving) development fees worth nearly $1 million. These bold moves are paying off for the community."</p>
		<p>Using smart growth principles O'Neil has increased the supply of affordable housing in the valley, cleaned up a contaminated site and created a neighborhood with access to trails and open space.</p>
		<p>"This neighborhood is helping to ensure that people who work here can afford to live here," said Mamula, who accompanied O'Neil to Washington. "These people are both the economic engine and the soul of the town."</p>
		<p>Eighty percent of the homes are reserved for people who work in Summit County - at about one-third the cost of the median purchase price of other homes in Breckenridge. Its location near downtown Breckenridge places housing near job centers, thus reducing commute times and related emissions.</p>
		<p>New homeowners include the town manager, government employees, shop owners, teachers and police officers.</p>
		<p>The plan includes public greens for outdoor play and connections for bicyclists and pedestrians.</p>
		<p>"It's a new model for affordable housing in a resort community," O'Neil said. "Previously, workers had a choice between a bed and a parking space in town or a long commute over mountain roads. Here, they can enjoy a sense of community in a real neighborhood."</p>
		<p>Last year, the Wellington Neighborhood was named to the Colorado Public Interest Research Group's Smart Growth Hall of Fame. The Denver Post editorial called the project "a model for the entire state."</p>
		<p>Whitman announced the creation of the national awards in January.</p> 
