					  <h2>French Gulch could see winter bus service</h2></center>

		 
		<p><b>by Jane Stebbins</b></p>
		 
		<p>BRECKENRIDGE - Residents in French Creek, Kenington Place, the Wellington Neighborhood, Gibson Heights and Vista Point might have town bus service next winter if Breckenridge town council members approve parking director Jim Benkelman's proposal to extend it there.</p>

		<p>"This has always been part of our plan," said David O'Neil, developer of the Wellington Neighborhood. "We're extremely excited this is happening. Imagine stepping out your front door and catching the free shuttle to City Market, downtown or Peak 8. This will be a great thing for all of us."</p>

		<p>The extension has been discussed by developers and town council members for the past two years as development in French Gulch increased. There are about 150 homes in the French Creek neighborhood, and new development farther down the gulch will more than double that.</p>

		<p>Town council members realize the three neighborhoods serve as a bedroom community to businesses in town. But they also know most of the workers drive to work - taking up valuable parking spaces merchants would like to have occupied by customers.</p>

		<p>Currently, thousands of vehicles use Reiling and Wellington roads into the gulch east of Breckenridge each day. Many residents have said they'd use a bus to get to town if one were routed through the neighborhoods.</p>

		<p>Bill Watterson, director of the Summit Stage, is one of them. Currently, he walks from his home in the Wellington Neighborhood to the Stage stop near the intersection of County Road 450 and Highway 9.</p>

		<p>"We're a one-car family," Watterson said. "Our girls go to school in Frisco in the  winter, so I share the ride because I work in Frisco. In the summer, they're not in school, so almost all the time I take the bus. I'm looking forward to the town  establishing service to the Wellington Neighborhood. That may become a new way to make that connection (to the Stage)."</p>

		<p>"I think bus service is really needed - anywhere," said Courtney Kenady, who hopes to move into the Wellington Neighborhood. "This neighborhood is so accessible, so convenient, the bus system is just the icing on the cake."</p>

		<p>"I think it would be really, really great," said Leslie DeBaggis, another area resident. "I can't park when I go to work, I can't park when I go to the ski area. It would be so much more convenient."</p>

		<p>It's far from being a done deal, however.</p>

		<p>According to Benkelman, town transit officials still need to figure out which hours would best serve the neighborhoods - and if the town has the funds to support another route.</p>

		<p>"Times are tight," Benkelman said. "We need to give the council options. We need to tell them, "To add this from this time to this time in the winter will be this amount of dollars, and year-round, here's what it's going to do to our budget'."</p>

		<p>It costs the town $42 an hour to run one bus around town, Benkelman said. That money covers employee pay, maintenance, fuel and administrative costs.</p>

		<p>"If we just added a couple hours in the morning, that's about $13,860," he said. "I want to give service to these folks out there, but I want to make sure it's going to work for everybody."</p>

		<p>Benkelman believes the neighborhoods could best be served by buses in the early-morning hours and then again in the evening. Town officials will distribute a survey to residents in about two weeks to see what their needs might be. For example, he said, residents might want bus service to get people home from town after work, but might not want that service to run until midnight, as it does on Peak 8.</p>

		<p>Benkelman said he thinks the best way to accommodate the new route would be to reduce half-hour service to Peak 8 and service it every hour instead. That bus could then be driven to French Gulch on the half-hour.</p>

