		  <h2>What made the Smart Growth award</h2>
		  </center>
		<p><b>November 21, 2002</b></p>
		<p><b>Sam Mamula</b></p>
		<p>Editor's note: Breckenridge won an Environmental Protection Agency (EPA) National Award for Smart Growth Achievement in the built projects category for the Wellington Neighborhood affordable housing project. Mayor Sam Mamula and Wellington builder David O'Neil accepted the award from EPA administrator Christine Todd Whitman. What follows is the text of Mamula's speech at the ceremony in Washington, D.C. Unfortunately, the Mamula ad-libs are missing.</p>
		<p>-------------</p>
		<p><img src="images/smart_photo.jpg" width="300" height="264" alt="" border="0" align="right">Breckenridge has the reputation for being the most popular ski area in North America. The reputation locals are most proud of is of being a real town with real people, not a contrived Bavarian ski village.</p>
		<p>That means a town with clean air, pure water and breathtaking vistas. These appealing qualities also made Breckenridge a Mecca for expensive second homes.</p>
		<p>Not only did this influx drive up the cost of housing, but it forced many locals to move out of town. These were locals who were not only the spirit of our town, but the people who made our town successful.</p>
		<p>We were threatened with the fact we were becoming a community of second homes that were vacant most of the year, and the only affordable housing was a rental unit.</p>
		<p>Another challenge we had was the Wellington-Oro Mine. The Oro was Summit County's largest producing mine from the late 1800s to the start of World War II. Despite the Oro's legacy, the fact was there were heavy metals leaching from the mine into French Creek and the Blue River, destroying the fish habitat, and unsightly mine tailings remained. Ironically, the land surrounding the mine has spectacular vistas and undisturbed habitat.</p>
		<p>These two challenges resulted in a unique cooperative partnership among the town of Breckenridge, Summit County, the Environmental Protection Agency, Colorado Department of Public Health and Environment and a private developer. The partnership was able to forge a unique vision with a simple three-legged stool approach.</p>
		<p>First - Clean up the spoiled areas left by the mining.</p>
		<p>Second - Build livable affordable housing through a brownfields model.</p>
		<p>Third - Preserve the 1,800 acres of surrounding private land to its natural state for recreation and open space.</p>
		<p>We're well on our way in our clean-up efforts. Eighty-five acres, which is the Wellington Neighborhood, has been remediated and an action plan for the mine water is being implemented.</p>
		<p>The second leg of the stool, for which we are being recognized today, is a real success story - an affordable housing project. The Wellington Neighborhood currently has 58 of the planned 120 homes completed, with a future potential of another 150 homes.</p>
		<p>The project, when finished, will have about 270 homes on 85 reclaimed, pristine acres, with parks and a significant open space dedication. We have property dedicated for a child care facility in an adjoining affordable housing project on another recycled mining site.</p>
		<p>The land for the Wellington Neighborhood, which had no density, was purchased for $1 million, which was contributed to partially fund the cleanup.</p>
		<p>The Wellington Neighborhood is a success, not only because it made a Brownfields habitable; it's successful because it has become an affordable neighborhood of homeowners who are local employees and business owners. In fact, our town manager, a county attorney, firefighters, police, school teachers and business owners have homes there.</p>
		<p>And finally, the third leg - 1,800 acres of surrounding land is under contract to the town of Breckenridge and Summit County for open space. We are in the process of continuing our unique partnership in planning the clean up and preservation of this important parcel with a private developer.</p>
		<p>The success of this venture was not solely the result of a unique partnership. It could not have been accomplished without the guidance and leadership of the EPA.</p>
		<p>Breckenridge provided density and significant subsidies and planning expertise, but probably the most important partner was the private sector.</p>
		<p>It kept the partnership together and moved forward despite the various unforeseen obstacles.</p>
		<p>Again, we thank you for awarding the town of Breckenridge this prestigious award, but more importantly, for EPA's support and guidance that made this possible.</p> 

