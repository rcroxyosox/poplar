<h2>The summers keep us here...</h2><p><img src="images/lifestyle2.jpg" alt="community" width="250px" /> </p>

  <p>You won&#39;t find us too far from home during the summertime in Breckenridge.  We don&#39;t spend much time doing yard work because most of the weekends are filled with music at the River Walk Center or at a Local Festival or BBQ.  You could possibly look for us down by the Blue River enjoying the world&#45;class fishing or Rafting down Ten Mile Creek or the Arkansas River just a short drive from town.  But mostly, you will find us outside in the mountains enjoying the scenery, wild flowers, riding, hiking or running on the trails we love.  There are more then 30 trail options within the town of Breckenridge that connect to a network of trails that will take you as far as Keystone, Frisco, Copper... there are endless possibilities!  It&#39;s a simple life, but it&#39;s extraordinary. </p>


