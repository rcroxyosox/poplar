		  <p><img src="images/lifestyle1.jpg" alt="community" width="250px" /> </p>
		  
		  <p>The first thing you notice about <strong>The Wellington Neighborhood</strong> is how the cozy Victorian style homes nestle up to the pine tree covered hills as if in a Currier &amp; Ives story, but the towering mountains are what command your attention and invigorate your mind...</p>
		  
		  <p>
		  	
There is an adventurous spirit in all of us who live to experience the incredible rush of a powder day, the heart-pounding speed of a mountain bike as it races down the B&amp;B trail in July, the tug of a fighting gold medal trout and the playful excitement of an attempt to outrun a torrential rainstorm. We live for the push of the last few hundred yards to summit a &#34;14er&#34; and can&#39;t imagine how the other half lives without the ability to enjoy the crisp mountain air, bluebird skies and the grand Rocky Mountains everyday.
		  
		  </p>

		  <h2>The winter brought us here...</h2>
<p>When you walk into a local coffee shop on a snowy November morning, the smell of fresh pastries and strong thick coffee fill your senses.  The smile that greets you at the counter will be zealous, but genuine.  The energy of the place is filled with excitement and anticipation for the upcoming winter season and fantasies of many powder days. 
</p>
