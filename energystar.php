				<h4>What does it mean to be ENERGY STAR&reg; qualified?</h4>
			
			
			<p style="font-size:medium; font-weight:normal">To earn the ENERGY STAR&reg;, a home must meet strict guidelines for energy efficiency set by the U.S. Environmental Protection Agency (EPA), making them 20&#45;30&#37; more efficient than standard homes.  </p>
			<p style="font-size:medium; font-weight:normal">This can save money on energy bills (billions each year nationally), provides a more comfortable living environment with better indoor air quality, and helps protect the environment. ENERGY STAR&reg; programs save so much energy, in fact, its equivalent to taking 30 million cars off the road each year.</p>
			
			<h5>Homes achieve this level of performance through a combination of energy&#45;efficient improvements including:</h5>
			<ul>
				<li><span style="font-size:medium; font-weight:normal">Effective Insulation Systems</span></li>
				<li><span style="font-size:medium; font-weight:normal">High&#45;Performance Windows</span></li>
				<li><span style="font-size:medium; font-weight:normal">Tight Construction and Ducts</span></li>
				<li><span style="font-size:medium; font-weight:normal">Efficient Heating and Cooling Equipment</span></li>
				<li><span style="font-size:medium; font-weight:normal">ENERGY STAR&reg; Qualified Lighting and Appliances</span></li>
			</ul>
			<p style="font-size:medium; font-weight:normal">All homes in The Wellington Neighborhood are third&#45;party inspected and HERS rated to meet the stringent ENERGY STAR&reg; standards required for certification &#8212; resulting in lower energy consumption and reduced green house gas emissions. Our Rater works closely with the builder throughout the construction process to help determine the needed energy&#45;saving equipment and construction techniques and conduct required on&#45;site diagnostic testing and inspections to document that the home is eligible to earn the ENERGY STAR&reg; label.</p>
			<h5>What does it mean to be HERS rated?</h5>
			<p style="font-size:medium; font-weight:normal"><strong>Home Energy Rating System (HERS)</strong> is a scoring system, which utilizes building plan analysis to identify any performance improvements, on&#45;site testing blower door testing to ensure efficiency, and construction inspections to come up with a HERS score, based on an Index of 100. The term &quot;Net Zero&quot; home comes from an energy home that is built with a goal of scoring a HERS Index of 0. The lower a home&#146;s HERS Index, the more energy efficient it is in comparison to the HERS Reference Home. Each 1 point decrease in the HERS Index corresponds to a 1&#37; reduction in energy consumption.</p>
			<p style="font-size:medium; font-weight:normal">For example, a HERS Index of 65 is 35&#37; more energy efficient than the HERS Reference Home.</p>
			
